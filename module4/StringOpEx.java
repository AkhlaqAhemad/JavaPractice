public class StringOpEx{
	public static void main(String[] args){
		String word = "Programming";
		System.out.println("Length of word: '"+word+"' is '"+word.length()+"'");
		System.out.println("First character of word: '"+word+"' is '"+word.charAt(0)+"'");
		System.out.println("Word: '" +word+"' is converted into Uppercase: '" +word.toUpperCase()+"'");
	}
}